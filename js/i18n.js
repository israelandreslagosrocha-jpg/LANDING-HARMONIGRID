/**
 * Translates all DOM elements matching [data-i18n] with the values from dictionary.
 * Uses TextContent to prevent XSS vulnerability vectors.
 * 
 * @param {string} lang Language code ('es', 'en', 'pt', 'fr', 'de', 'it')
 */
export function translatePage(lang) {
  // Safe fallback handling
  const selectedLang = translations[lang] ? lang : 'es';
  const dict = translations[selectedLang];

  // Set the global html lang attribute safely
  document.documentElement.setAttribute('lang', selectedLang);

  // Find all translatable elements
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // Security: use textContent to avoid XSS injections
      el.textContent = dict[key];
    }
  });

  // Handle placeholders safely
  const inputs = document.querySelectorAll('[data-i18n-placeholder]');
  inputs.forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) {
      el.setAttribute('placeholder', dict[key]);
    }
  });

  // Handle HTML content safely ONLY for trusted strings that contain tags, like social-proof-text.
  // Instead of innerHTML, we sanitize or handle explicitly:
  const socialProofEl = document.querySelector('[data-i18n-html]');
  if (socialProofEl) {
    const key = socialProofEl.getAttribute('data-i18n-html');
    if (dict[key]) {
      // In this specific trust string, we have "<strong>2,500 members</strong>" or "<strong>2.500 miembros</strong>".
      // We parse or set safely. Since this is hardcoded in our translation dictionary and not user-controlled,
      // it is safe to set, but to be absolutely XSS-proof, we replace the element's content using structured DOM node building:
      const parts = dict[key].split(/<strong>(.*?)<\/strong>/);
      socialProofEl.textContent = ''; // clear
      
      if (parts.length > 1) {
        // parts[0] is text before <strong>, parts[1] is text inside <strong>, parts[2] is text after
        socialProofEl.appendChild(document.createTextNode(parts[0]));
        const strong = document.createElement('strong');
        strong.textContent = parts[1];
        socialProofEl.appendChild(strong);
        if (parts[2]) {
          socialProofEl.appendChild(document.createTextNode(parts[2]));
        }
      } else {
        socialProofEl.textContent = dict[key];
      }
    }
  }

  // Update language select button label
  const langLabelMap = {
    es: 'Español',
    en: 'English',
    pt: 'Português',
    fr: 'Français',
    de: 'Deutsch',
    it: 'Italiano'
  };
  const activeLangText = document.getElementById('active-lang-text');
  if (activeLangText) {
    activeLangText.textContent = langLabelMap[selectedLang];
  }

  // Toggle active class on option list
  document.querySelectorAll('.lang-option').forEach((opt) => {
    if (opt.getAttribute('data-lang') === selectedLang) {
      opt.classList.add('active');
    } else {
      opt.classList.remove('active');
    }
  });

  // Fire a custom event to notify other modules (like synth pad or forms) about language change
  const event = new CustomEvent('languageChanged', { detail: { lang: selectedLang } });
  document.dispatchEvent(event);
}
